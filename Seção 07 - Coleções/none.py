"""
Tipo None

O tipo de dado None em Python representa o tipo sem tipo, ou poderia ser conhecido também
como tipo vaziom, porém, falar que é um tipo sem tipo é mais apropriado

OBS: O tipo None é SEMPRE especificado com a primeira letra maiúscula

Certo: None
Errado: none

Quando utiliza-se ?
    - Podemos utilizar None quando queremos criar uma variável e inicializa-lo com um tipo sem tipo, antes
    de recebermos um valor final.

OBS: O tipo None é sempre considerddo FALSO
"""

num = None

print(type(num))

num = 1.44

print(type(num))